	private void populateOverallStagingToScreen(TumourGroupSiteOverallStagingVoCollection listOverall, boolean fromOpen)
	{
		// Clear grid rows
		form.dyngrdOverall().getRows().clear();
		
		// Check collection - if it is null terminate function
		if (listOverall == null)
			return;
		
		
		for (TumourGroupSiteOverallStagingVo overall : listOverall)
		{
			// If we populate on open mark all records as having been saved
			// This will ensure that records with a value will be read-only when column sorting the grid
			if (fromOpen)
			{
				overall.setIsSaved(Boolean.TRUE);
			}
				
			addNewOverallStaging(overall);
		}
	}

	/**
	 * @param overall
	 */
	private void addNewOverallStaging(TumourGroupSiteOverallStagingVo overall)
	{
		if (overall == null)
			return;
		
		// Create new row for each value in collection
		DynamicGridRow overallRow = form.dyngrdOverall().getRows().newRow();
		
		// Create cell for T Value
		DynamicGridColumn column = form.dyngrdOverall().getColumns().getByIdentifier(COL_T_VALUE);
		DynamicGridCell cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(overall.getTValIsNotNull() ? overall.getTVal().getTNMValue() : "");
		cell.setReadOnly(true);
		
		// Create cell for N Value
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_N_VALUE);
		cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(overall.getNValIsNotNull() ? overall.getNVal().getTNMValue() : "");
		cell.setReadOnly(true);

		// Create cell for M Value
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_M_VALUE);
		cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(overall.getMValIsNotNull() ? overall.getMVal().getTNMValue() : "");
		cell.setReadOnly(true);
		
		
		// Create cell for Histology Type value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresHisto()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_HIST_TYPE);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);
			
			if (overall.getHistValIsNotNull())
				cell.setValue(overall.getHistVal().getAssociatedHistology().getHistologyDescription());
		}
		
		// Create cell for Histology Grade value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresDiff()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_HIST_GRADE);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);
			
			if (overall.getDiffValIsNotNull())
				cell.setValue(overall.getDiffVal().getTumourDifferentation().getGrade());
		}

		// Create cell for Serum Marker value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresSMarkers()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_SERUM);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);

			if (overall.getSerumMarkerValIsNotNull())
				cell.setValue(overall.getSerumMarkerVal().getSerumMarkerValue());
		}
		
		// Create cell for Over45 value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresOver45()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_OVER_45);
			cell = overallRow.getCells().newCell(column, DynamicCellType.BOOL);
			cell.setReadOnly(true);

			if (overall.getO45IsNotNull())
				cell.setValue(overall.getO45().equals("Y") ? Boolean.TRUE : Boolean.FALSE);
		}
		
		// Create cell for Overall staging value
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_OVERALL);
		if (!Boolean.TRUE.equals(overall.getIsSaved()) || !overall.getOverallStageDescriptionIsNotNull() || (TumourOverallStage.TOBECONFIGURED.equals(overall.getOverallStageDescription()) && Boolean.TRUE.equals(overall.getIsActive())))
		{
			cell = overallRow.getCells().newCell(column, DynamicCellType.ENUMERATION);
			bindOverallValue(cell);
			cell.setValue(overall.getOverallStageDescription());
			cell.setReadOnly(false);
			cell.setAutoPostBack(true);
		}
		else
		{
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setValue(overall.getOverallStageDescription().getText());
			cell.setReadOnly(true);
		}
		
		
		// Set row background for inactive records
		if (!Boolean.TRUE.equals(overall.getIsActive()))
			overallRow.setBackColor(Color.Crimson);
		
		// Set overall record to row
		overallRow.setValue(overall);
	}

	/**
	 * <b>WDEV-12569</b>
	 * Function used to retrieve Overall Staging collection from screen
	 * @param setToBeConfigured - if set to <b>true</b>, function will replace <b>null</b> Overall Stage Description values with <b>TOBECONFIGURED</b> (-2151)
	 *  
	 * @return Overall Staging records collection configured for Tumour Group
	 */
	private TumourGroupSiteOverallStagingVoCollection populateOverallStagingFromScreen(boolean setToBeConfigured)
	{
		TumourGroupSiteOverallStagingVoCollection overallStaging = new TumourGroupSiteOverallStagingVoCollection();

		for (int i = 0; i < form.dyngrdOverall().getRows().size(); i++)
		{
			if (form.dyngrdOverall().getRows().get(i).getValue() instanceof TumourGroupSiteOverallStagingVo)
			{
				TumourGroupSiteOverallStagingVo staging = (TumourGroupSiteOverallStagingVo) form.dyngrdOverall().getRows().get(i).getValue();
				
				if (setToBeConfigured && !staging.getOverallStageDescriptionIsNotNull())
					staging.setOverallStageDescription(TumourOverallStage.TOBECONFIGURED);

				overallStaging.add(staging);
			}
		}
		
		return overallStaging;
	}


	private void addDynamicGridRow(SelectedRecordForOverallStagingDialogVo overall)
	{
		if (overall == null)
			return;

		// Create Overall VO to be added to dynamic grid
		TumourGroupSiteOverallStagingVo tumourOverall = new TumourGroupSiteOverallStagingVo();
	
		// HACK because the TumourGoupOverall Staging is discarding tumour group and tumour site
		tumourOverall.setTGroup(form.getGlobalContext().ClinicalAdmin.getTumourGroup());
		tumourOverall.setTSite(null);
		
		tumourOverall.setIsActive(Boolean.TRUE);

		// Get TNM values
		for (TumourGroupSiteTNMValueListVo tumourGroupSiteTNM : overall.getTNMValues())
		{
			if (TNMType.T.equals(tumourGroupSiteTNM.getTNMType()))
			{
				tumourOverall.setTVal(tumourGroupSiteTNM);
				continue;
			}
			
			if (TNMType.N.equals(tumourGroupSiteTNM.getTNMType()))
			{
				tumourOverall.setNVal(tumourGroupSiteTNM);
				continue;
			}
			
			if (TNMType.M.equals(tumourGroupSiteTNM.getTNMType()))
			{
				tumourOverall.setMVal(tumourGroupSiteTNM);
			}
		}
		
		// Get Histology Type
		tumourOverall.setHistVal(overall.getHistologyValue());
		// Get Histology Grade
		tumourOverall.setDiffVal(overall.getDifferentationValue());
		// Get Serum Marker
		tumourOverall.setSerumMarkerVal(overall.getSerumMarker());
		// Get Over 45
		tumourOverall.setO45(Boolean.TRUE.equals(overall.getOver45()) ? "Y" : "N");
		
		// Get Overall Staging
		tumourOverall.setOverallStageDescription(overall.getOverallValue());
		
		addNewOverallStaging(tumourOverall);
	}


	private void generateDefaultRecords(TumourGroupVo tumourGroup)
	{
		// Check group parameter
		if (tumourGroup == null)
			return;
		
		// Separate T, N and M collection values
		TumourGroupSiteTNMValueListVoCollection TValues = new TumourGroupSiteTNMValueListVoCollection();
		TumourGroupSiteTNMValueListVoCollection NValues = new TumourGroupSiteTNMValueListVoCollection();
		TumourGroupSiteTNMValueListVoCollection MValues = new TumourGroupSiteTNMValueListVoCollection();
		
		if (tumourGroup.getTNMValuesIsNotNull())
		{
			for (TumourGroupSiteTNMValueListVo tumourGroupSiteTNM : tumourGroup.getTNMValues())
			{
				// WDEV-13259
				// Pathological T, N, M values are not used for OverallStaging so exclude them
				if (TNMClinicalpathological.PATHOLOGICALONLY.equals(tumourGroupSiteTNM.getClinicalPathological()))
					continue;
				
				if (!Boolean.TRUE.equals(tumourGroupSiteTNM.getIsActive()))
					continue;
				
				if (TNMType.T.equals(tumourGroupSiteTNM.getTNMType()))
				{
					TValues.add(tumourGroupSiteTNM);
					continue;
				}
				
				if (TNMType.N.equals(tumourGroupSiteTNM.getTNMType()))
				{
					NValues.add(tumourGroupSiteTNM);
					continue;
				}
				
				if (TNMType.M.equals(tumourGroupSiteTNM.getTNMType()))
				{
					MValues.add(tumourGroupSiteTNM);
				}
			}
		}
		
		// Check T values
		if (TValues == null || TValues.size() == 0)
		{
			engine.showMessage("No TNM T Type values exist and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
			form.chkGenerate().setValue(false);
			return;
		}
		// Check N values
		if (NValues == null || NValues.size() == 0)
		{
			engine.showMessage("No TNM N Type values exist and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
			form.chkGenerate().setValue(false);
			return;
		}
		// Check M values
		if (MValues == null || MValues.size() == 0)
		{
			engine.showMessage("No TNM M Type values exist and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
			form.chkGenerate().setValue(false);
			return;
		}
		
		
		// Get pointers to relevant collection
		TumourGroupHistologyVoCollection histopatologicType = tumourGroup.getHistologies();
		TumourGroupHistopathologyGradeVoCollection histopathologicGrades = tumourGroup.getHistopathologicGrades();
		TumourSerumMarkersLiteVoCollection serumMarkers = tumourGroup.getSerumMarkers();
		
		int histologiesTypeSize = 1;
		int histologiesGradeSize = 1;
		int serumMarkersSize = 1;
		int over45Size = 1;
		
		// Get size for relevant collection (Put 1 where the collection is not to be considered)
		// Also perform validation (if a collection has no values and it is required)
		boolean histologicTypeRequired = Boolean.TRUE.equals(tumourGroup.getTnmRequiresHisto());
		boolean histologicGradeRequired = Boolean.TRUE.equals(tumourGroup.getTnmRequiresDiff());
		boolean serumMarkerRequired = Boolean.TRUE.equals(tumourGroup.getTnmRequiresSMarkers());
		boolean over45Required = Boolean.TRUE.equals(tumourGroup.getTnmRequiresOver45());

		if (histologicTypeRequired)
		{
			if (!tumourGroup.getHistologiesIsNotNull() || tumourGroup.getHistologies().size() == 0)
			{
				engine.showMessage("No Histology Type values exist and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
				form.chkGenerate().setValue(false);
				return;
			}
			
			histologiesTypeSize = tumourGroup.getHistologies().size();
		}
		
		
		if (histologicGradeRequired)
		{
			if (!tumourGroup.getHistopathologicGradesIsNotNull() || tumourGroup.getHistopathologicGrades().size() == 0)
			{
				engine.showMessage("No Histology Grade values exist for the chosen Site and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
				form.chkGenerate().setValue(false);
				return;
			}
			
			histologiesGradeSize = tumourGroup.getHistopathologicGrades().size();
		}
		
		if (serumMarkerRequired)
		{
			if (!tumourGroup.getSerumMarkersIsNotNull() || tumourGroup.getSerumMarkers().size() == 0)
			{
				engine.showMessage("No Serum Markers values exist and they are required to create Overall Staging values.", "Missing data", MessageButtons.OK, MessageIcon.WARNING);
				form.chkGenerate().setValue(false);
				return;
			}
		}
		
		if (over45Required)
			over45Size = 2;
		
		
		for (int indexTVal = 0; indexTVal < TValues.size(); indexTVal++)
			for (int indexNVal = 0; indexNVal < NValues.size(); indexNVal++)
				for (int indexMVal = 0; indexMVal < MValues.size(); indexMVal++)
					for (int indexHistoType = 0; indexHistoType < histologiesTypeSize; indexHistoType++)
					{
						TumourGroupHistologyVo histologyType = null;
						if (histologicTypeRequired && histopatologicType.size() > 0)
						{
							// Get histology type record
							histologyType = histopatologicType.get(indexHistoType);

							// Skip for inactive histology type
							if (!Boolean.TRUE.equals(histologyType.getIsActive()))
								continue;
						}
						
						for (int indexHistoGrade = 0; indexHistoGrade < histologiesGradeSize; indexHistoGrade++)
						{
							TumourGroupHistopathologyGradeVo histologyGrade = null;
							if (histologicGradeRequired && histopathologicGrades.size() > 0)
							{
								// Get histology grade record
								histologyGrade = histopathologicGrades.get(indexHistoGrade);

								// Skip for inactive histology grade
								if (!Boolean.TRUE.equals(histologyGrade.getIsActive()))
									continue;
							}
							
							for (int indexSerumMarker = 0; indexSerumMarker < serumMarkersSize; indexSerumMarker++)
							{
								TumourSerumMarkersLiteVo serumMarker = null;
								if (serumMarkerRequired && serumMarkers.size() > 0)
								{
									// Get serum marker
									serumMarker = serumMarkers.get(indexSerumMarker);

									// Skip for inactive serum marker
									if (!Boolean.TRUE.equals(serumMarker.getIsActive()))
										continue;
								}
								
								for (int indexOver45 = 0; indexOver45 < over45Size; indexOver45++)
								{
									addOverallStagingRow(TValues.get(indexTVal), NValues.get(indexNVal), MValues.get(indexMVal),			// For T, N and M values no check is required = they are null
															histologyType,																	// Check if Histology Type is required for overall staging before passing value
															histologyGrade,																	// Check if Histology Grade is required for overall staging before passing value
															serumMarker,																	// Check if Serum Marker is required for overall staging before passing value
															over45Required ? (indexOver45 == 0 ? Boolean.FALSE : Boolean.TRUE) : null); 	// Check if Over 45 is relevant for overall staging before passing value
								}
							}
						}
					}
	}

	private void addOverallStagingRow(TumourGroupSiteTNMValueListVo tValue, TumourGroupSiteTNMValueListVo nValue, TumourGroupSiteTNMValueListVo mValue,
										TumourGroupHistologyVo histology, TumourGroupHistopathologyGradeVo histologyGrade, TumourSerumMarkersLiteVo serumMarkers, Boolean over45)
	{
		// Create new row
		DynamicGridRow overallRow = form.dyngrdOverall().getRows().newRow();
		
		// Create overall value
		TumourGroupSiteOverallStagingVo overall = new TumourGroupSiteOverallStagingVo();
		
		// Create cell for T value
		DynamicGridColumn column = form.dyngrdOverall().getColumns().getByIdentifier(COL_T_VALUE);
		DynamicGridCell cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(tValue != null ? tValue.getTNMValue() : "");
		cell.setReadOnly(true);
		overall.setTVal(tValue);

		// Create cell for T value
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_N_VALUE);
		cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(nValue != null ? nValue.getTNMValue() : "");
		cell.setReadOnly(true);
		overall.setNVal(nValue);

		// Create cell for T value
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_M_VALUE);
		cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
		cell.setValue(mValue != null ? mValue.getTNMValue() : "");
		cell.setReadOnly(true);
		overall.setMVal(mValue);

		// Create cell for Histology Type value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresHisto()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_HIST_TYPE);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);
			
			if (histology != null)
			{
				cell.setValue(histology.getAssociatedHistology().getHistologyDescription());
				overall.setHistVal(histology);
			}
		}
		
		// Create cell for Histology Grade value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresDiff()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_HIST_GRADE);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);
			
			if (histologyGrade != null)
			{
				cell.setValue(histologyGrade.getTumourDifferentation().getGrade());
				overall.setDiffVal(histologyGrade);
			}
		}

		// Create cell for Serum Marker value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresSMarkers()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_SERUM);
			cell = overallRow.getCells().newCell(column, DynamicCellType.STRING);
			cell.setReadOnly(true);

			if (serumMarkers != null)
			{
				cell.setValue(serumMarkers.getSerumMarkerValue());
				overall.setSerumMarkerVal(serumMarkers);
			}
		}
		
		// Create cell for Over45 value if needed
		if (Boolean.TRUE.equals(form.getGlobalContext().ClinicalAdmin.getTumourGroup().getTnmRequiresOver45()))
		{
			column = form.dyngrdOverall().getColumns().getByIdentifier(COL_OVER_45);
			cell = overallRow.getCells().newCell(column, DynamicCellType.BOOL);
			cell.setReadOnly(true);

			cell.setValue(over45);
			overall.setO45(Boolean.TRUE.equals(over45) ? "Y" : "N");
		}
		
		column = form.dyngrdOverall().getColumns().getByIdentifier(COL_OVERALL);
		cell = overallRow.getCells().newCell(column, DynamicCellType.ENUMERATION);
		cell.setAutoPostBack(true);

		bindOverallValue(cell);
		cell.setValue(LookupHelper.getTumourOverallStage(domain.getLookupService()));
		cell.setIdentifier(LookupHelper.getTumourOverallStage(domain.getLookupService()));

		overall.setIsActive(Boolean.TRUE);
		overall.setTGroup(form.getGlobalContext().ClinicalAdmin.getTumourGroup());
		overall.setTSite(null);

		overallRow.setValue(overall);
	}

	protected final void bindOverallValue(DynamicGridCell typeCell)
	{
		TumourOverallStageCollection lookupCollection = LookupHelper.getTumourOverallStage(domain.getLookupService());

		for (int x = 0; x < lookupCollection.size(); x++)
		{
			// Replaced @deprecated method
			// typeCell.getItems().newItem(lookupCollection.get(x), lookupCollection.get(x).getImage(), lookupCollection.get(x).getTextColor());
			typeCell.getItems().newItem(lookupCollection.get(x), lookupCollection.get(x).getText(), lookupCollection.get(x).getImage(), lookupCollection.get(x).getTextColor());
		}
	}
