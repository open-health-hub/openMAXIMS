	public VitalSignsVo getVitalSignsById(VitalSignsRefVo vitalSignsRef) 
	{	
		if(vitalSignsRef == null || !vitalSignsRef.getID_VitalSignsIsNotNull())
			return null;
		
		DomainFactory factory = getDomainFactory();
		
		List vitalSign =   factory.find("from VitalSigns as v1_1 where v1_1.id = :idVitalSign",
				new String[]{"idVitalSign" },
				new Object[]{ vitalSignsRef.getID_VitalSigns()});

		VitalSignsVoCollection voCollVitalSign = VitalSignsVoAssembler.createVitalSignsVoCollectionFromVitalSigns(vitalSign);
				
		if(voCollVitalSign.size()>0)
			return voCollVitalSign.get(0);
		else 
			return null;
	}

	
	public VitalSignsLiteVoCollection listVitalSignsLiteVo(CareContextRefVo refCareContext) 
	{		
		if(refCareContext == null || !refCareContext.getID_CareContextIsNotNull() )
			return null;
		
		DomainFactory factory = getDomainFactory();
		
		List vitalSignsLite =   factory.find("select v1_1 from VitalSigns as v1_1 left join v1_1.careContext as c1_1 where c1_1.id = :idCareContext",
				new String[]{"idCareContext" },
				new Object[]{ refCareContext.getID_CareContext()});

		VitalSignsLiteVoCollection voCollVitalSignsLite = VitalSignsLiteVoAssembler.createVitalSignsLiteVoCollectionFromVitalSigns(vitalSignsLite);
		
		if(voCollVitalSignsLite.size()>0)
		{
			voCollVitalSignsLite.sort(SortOrder.DESCENDING);
			return voCollVitalSignsLite;
		}
		else 
			return null;

	}
	
	

